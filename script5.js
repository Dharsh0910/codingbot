document.addEventListener("DOMContentLoaded", function() {
    const apiKey = 'e1d2b972be24f2a94066140a';  
    const form = document.getElementById('converter-form');
    const resultDiv = document.getElementById('result');
   
   
    const currencyOptions = ['USD', 'EUR', 'GBP', 'INR', 'AUD', 'CAD', 'SGD', 'JPY'];
    const fromCurrencySelect = document.getElementById('from-currency');
    const toCurrencySelect = document.getElementById('to-currency');
    
    currencyOptions.forEach(currency => {
        let option1 = document.createElement('option');
        option1.value = currency;
        option1.textContent = currency;
        fromCurrencySelect.appendChild(option1);
        
        let option2 = document.createElement('option');
        option2.value = currency;
        option2.textContent = currency;
        toCurrencySelect.appendChild(option2);
    });

    form.addEventListener('submit', function(event) {
        event.preventDefault();
        
        const amount = document.getElementById('amount').value;
        const fromCurrency = document.getElementById('from-currency').value;
        const toCurrency = document.getElementById('to-currency').value;

        convertCurrency(apiKey, fromCurrency, toCurrency, amount)
            .then(convertedAmount => {
                resultDiv.textContent = `${amount} ${fromCurrency} is ${convertedAmount.toFixed(2)} ${toCurrency}`;
            })
            .catch(error => {
                resultDiv.textContent = `Error: ${error.message}`;
            });
    });

    async function convertCurrency(apiKey, fromCurrency, toCurrency, amount) {
        const url = ` https://v6.exchangerate-api.com/v6/e1d2b972be24f2a94066140a/latest/USD`;
        const response = await fetch(url);
        const data = await response.json();

        if (data.result !== 'success') {
            throw new Error(data['error-type']);
        }

        const rate = data.conversion_rates[toCurrency];
        return amount * rate;
    }
});

















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































